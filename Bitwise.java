package Operators;

public class Bitwise {
	public static void main(String[] args) {
		int a=1;
		int b=2;
		int c=a|b; //OR
		int v=a&b; //AND
		int s=a^b; //XOR
		int q=~a; //Negation
		System.out.println(c);
		System.out.println(v);
		System.out.println(s);
		System.out.println(q);
		
		
	}

}
